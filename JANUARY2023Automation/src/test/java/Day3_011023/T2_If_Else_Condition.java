package Day3_011023;

public class T2_If_Else_Condition {
    public static void main(String[] args) {

        //on if condition print only when a is less than b

        int a = 1;
        int b = 3;

        if (a < b){
            System.out.println("a is less than b");
        }//end of if
        //else condition will handle when a is greater or equal to b
        else {
            System.out.println("a is either greater than or equal to b");
        }//end of else
    }//end of main
}//end of class
